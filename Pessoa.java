public class Pessoa {
    public int cpf;
    public int rg;
    public String nome;   
    public String nasc;
    public String cidade;

    public Pessoa(int cpf, int rg, String nome, String nasc, String cidade) {
        this.cpf = cpf;
        this.rg = rg;
        this.nome = nome;
        this.nasc = nasc;
        this.cidade = cidade;
    }
}